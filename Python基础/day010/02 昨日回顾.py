# 函数调用 func()
# 作用：调用函数+接收返回值

# return
# 将返回值返回给函数调用者
# 函数执行完后自动销毁函数体中开辟的空间
# 终止当前函数，return下方代码不执行
# 不写return默认返回None，写return不写返回值也是None
# 可以返回任意、多个数据类型（以元组的形式存储）
# 可以写多个return ，只执行一个
# 函数体中存放的是代码，只有函数被调用时，函数体中的代码才会被执行

# 函数的参数
# 形参:函数定义阶段的参数
    # 位置参数:必须一一对应
    # 默认参数:可传(覆盖默认值)可不传(使用默认值)
    # 位置参数 > 默认参数
# 实参:函数调用阶段的参数
# 位置参数:必须一一对应
# 关键字参数:指定关键字传参
# 位置参数 > 关键字参数


